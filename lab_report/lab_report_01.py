import numpy as np
from matplotlib import pyplot as plt

# Ausgabe ist unten!
print("1.1.1 Wie konnen auf einfachem Weg die Zahlen von 100 bis 200 erzeugt werden?")
a1 = np.arange(100, 201)
print(a1)
print()

print("1.1.2 Wie konnen die Zahlen 100 bis 200 mit Abstand 2 (also 100, 102, 104,..) erzeugt werden?")
a2 = np.arange(100, 201, 2)
print(a2)
print()

print("1.1.3 Wie bekommt man Zahlen zwischen 100 und 110 mit Abstand 0.5, also 100, 100.5,101,...?")
a3 = np.linspace(100, 110, 21)
print(a3)
print()

print("1.1.4.1 Erzeuge 100 Normalverteilte Zahlen")
a4 = np.random.normal(50, 10, 100)
plt.hist(a4, bins=20)
plt.title("Normalverteilung")
plt.savefig("./out/normal.png")
plt.show()
print(a4)
print()

print("1.1.4.2 Erzeuge 100 Gleichverteilte Zahlen")
a5 = np.random.uniform(0, 100, 100)
plt.hist(a5, bins=20)
plt.title("Gleichverteilung")
plt.savefig("./out/uniform.png")
print(a5)
plt.show()
print()

print("2.1 Folgende Kennzahlen berechnen: Mittelwert, Median, Minimum, Maximum, Standardabweichung")
print("Mittelwert:")
b11 = a4.mean()
print(b11)
print()

print("Median:")
b12 = np.median(a4)
print(b12)
print()

print("Minimum:")
b13 = a4.min()
print(b13)
print()

print("Maximum:")
b14 = a4.max()
print(b14)
print()

print("Standardabweichung:")
b15 = np.std(a4)
print(b15)
print()

print("2.2 In welchem Bereich liegen die mittleren 95%? Welche Werte sind das die ausserhalb liegen?")
b2 = np.mean(a4) - np.std(a4) * 2, np.mean(a4) + np.std(a4) * 2
print(b2)
print()

print("2.3 Alle Zahlen mit 100 multiplizieren")
b3 = a4 * 100
print(b3)
print()

print("2.4 Nur die ersten 10 Zahlen auswahlen")
b4 = a4[:10]
print(b4)
print()

print("2.5 Nur die Werte auswahlen, die grosser 50 (0 wäre zu klein) sind")
b5 = a4 > 50
print(a4[b5])

